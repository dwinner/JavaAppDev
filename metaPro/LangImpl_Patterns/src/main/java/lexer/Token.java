public class Token {
    protected int type;
    protected String text;

    public Token(int type, String text) {
        this.type = type;
        this.text = text;
    }

    public int getType() {
        return type;
    }

    public String getText() {
        return text;
    }

    public String toString() {
        return "<'" + text + "'," + ListLexer.tokenNames[type] + ">";
    }
}